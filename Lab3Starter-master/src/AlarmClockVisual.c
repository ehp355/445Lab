void paintDigitalClock(uint32_t hour, uint32_t min, uint32_t hourColor, uint32_t minColor){
	min = min% 60;
	hour = hour % 12;
	if(hour==0){
		hour = 12;
	}
	char zero = '0';
	char minuteChar1= ((char)(min%10))+zero;
	char minuteChar2= ((char)(min/10))+zero;
	char hourChar1= ((char)(hour%10))+zero;
	char hourChar2= ((char)(hour/10)+zero);
	char colin = ':';
	
	ST7735_DrawCharS(20, 70, hourChar2, hourColor, ST7735_BLACK, 3);
	ST7735_DrawCharS(40, 70, hourChar1, hourColor, ST7735_BLACK, 3);
	ST7735_DrawCharS(60, 70, colin, ST7735_BLUE, ST7735_BLACK, 3);
	ST7735_DrawCharS(80, 70, minuteChar2, minColor, ST7735_BLACK, 3);
	ST7735_DrawCharS(100, 70, minuteChar1, minColor, ST7735_BLACK, 3);
}

